menuItems.forEach((menuItem) => {
  menuItem.addEventListener("click", (e) => {
      e.preventDefault();

      const targetId = e.target.getAttribute("href").replace("#","");

      submenus.forEach((menu_section) => {
      menu_section.classList.remove("active");
      });

      const targetMenu = document.getElementById(targetId + "-menu");
      if (targetMenu) {
      targetMenu.classList.add("active");
      }

      if (contentMap[targetId]) {
      mainContent.innerHTML = contentMap[targetId];
      } 
      else {
      mainContent.innerHTML="<p>Η ενότητα δεν είναι διαθέσιμη.</p>";
      }
    });
});

