const loadExhibitions = (category) => {
  fetch('http://localhost:3000/api/exhibitions')
    .then((response) => {
      if (!response.ok) {
        throw new Error('Σφάλμα στη φόρτωση δεδομένων.');
      }
      return response.json();
    })
    .then((data) => {
      const mainContent = document.getElementById('main_content');
      let content = '';

      if (category === 'local_exhibitions') {
        // Τοπικές Εκθέσεις
        content += '<h3>Τοπικές Εκθέσεις</h3><ul>';
        data.local_exhibitions.forEach((exhibition) => {
          content += `<li>${exhibition.title} - ${exhibition.year} (${exhibition.location})</li>`;
        });
        content += '</ul>';
      } else if (category === 'international_exhibitions') {
        // Διεθνείς Εκθέσεις
        content += '<h3>Διεθνείς Εκθέσεις</h3><ul>';
        data.international_exhibitions.forEach((exhibition) => {
          content += `<li>${exhibition.title} - ${exhibition.year} (${exhibition.location})</li>`;
        });
        content += '</ul>';
      }

      mainContent.innerHTML = content;
    })
    .catch((err) => {
      console.error('Σφάλμα κατά τη φόρτωση των εκθέσεων:', err);
      document.getElementById('main_content').innerHTML = '<p>Δεν ήταν δυνατή η φόρτωση των δεδομένων.</p>';
    });
};

document.querySelectorAll('aside a').forEach((link) => {
  link.addEventListener('click', (e) => {
    e.preventDefault();

    const category = e.target.getAttribute('href').replace('#', ''); 

    if (category === 'local_exhibitions' || category === 'international_exhibitions') {
      loadExhibitions(category); // Κλήση με την κατάλληλη κατηγορία
    }
  });
});

const loadLinks = (category) => {
  fetch('http://localhost:3000/api/links')
    .then((response) => {
      if (!response.ok) {
        throw new Error('Σφάλμα στη φόρτωση δεδομένων.');
      }
      return response.json();
    })
    .then((data) => {
      const mainContent = document.getElementById('main_content');
      let content = `<h3>${category === "online_links" ? "Διαδικτυακοί Σύνδεσμοι" : "Σύνδεσμοι Βιβλιογραφίας"}</h3><ul>`;
      const links = category === "online_links" ? data.online_links : data.bibliography_links;

      links.forEach((link) => {
        content += `<li><a href="${link.url}" target="_blank">${link.title}</a></li>`;
      });
      content += "</ul>";
      mainContent.innerHTML = content;
    })
    .catch((err) => {
      console.error("Σφάλμα κατά τη φόρτωση των συνδέσμων:", err);
      document.getElementById('main_content').innerHTML = "<p>Δεν ήταν δυνατή η φόρτωση των δεδομένων.</p>";
    });
};

